#include <stdio.h>//link section
#include <stdio.h>//link section
#define PI 3.142//Global declaration part

int main()
{
float C,A,r;//declaration part

printf("Enter the radius\n");//executable part
scanf("%f",&r);
printf("\n");

C=2*PI*r;//Formula for Circumference
printf("Circumference = %f units \n",C);//executable part
printf("\n");

A=PI*r*r;//Formula for Area
printf("Area = %f unit^2 \n",A);//executable part
printf("\n");

return 0;
}/*Activity 2.7: Program to find the Circumference
 and Area of the circle; F-60 ASHISH VAIDYA.*/








